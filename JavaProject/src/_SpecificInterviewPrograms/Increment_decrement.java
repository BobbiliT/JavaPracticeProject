package _SpecificInterviewPrograms;

public class Increment_decrement {
public static void main(String[] args) {
	int a=10;
	int b=20;
	System.out.println(a++); // this is post increment
	System.out.println(++a); // this is pre-increment
	System.out.println(b--); // this is post decrement
	System.out.println(--b); // this is pre-decrement
}
}